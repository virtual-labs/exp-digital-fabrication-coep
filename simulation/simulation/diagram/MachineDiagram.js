var counter = 0;
var position;
var currentPath = 0;
var dirForward = true;
function method_animasi()
{
   if (counter >= paths[currentPath].getTotalLength() && dirForward)
   {
       dirForward = false;
   }
   else if (counter == 0 && !dirForward)
   {
       var newPath = currentPath;
      
       currentPath = newPath;
       dirForward = true;
   }
   else
   {
      // position = paths[currentPath].getPointAtLength(counter);
     //  obj.attr({cx: position.x, cy: position.y});
     
     		obj.remove();
      //   obj1.remove();
   position = paths[currentPath].getPointAtLength(counter);
  /*   obj.attr({
       cx: position.x  ,
       cy: position.y 
        }); 
    */
   x_path = position.x  
   y_path =  position.y 
    
        drawCuttingTool(x_path, y_path); 
      //    box(x_path, y_path); 
   }
   if (dirForward)
   {
       counter++;
   }
   else
   {
       counter--;
   }
};


